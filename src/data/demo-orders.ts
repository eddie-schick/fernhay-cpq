// ─── Status-to-pipeline mapping ─────────────────────────────────────────────

type StatusName =
  | "Quote Generated"
  | "Quote Accepted"
  | "Order Processing"
  | "In Production"
  | "In Transit"
  | "Delivered";

function statusForStage(stage: number): StatusName {
  if (stage <= 1) return "Quote Generated";
  if (stage === 2) return "Quote Accepted";
  if (stage <= 4) return "Order Processing";
  if (stage <= 6) return "In Production";
  if (stage <= 9) return "In Production";
  if (stage <= 11) return "In Transit";
  return "Delivered";
}

// ─── Pipeline History Builder ───────────────────────────────────────────────

function buildPipelineHistory(
  currentStage: number,
  startDate: string,
  daysPerStage: number[],
): OrderWithPipeline["pipelineHistory"] {
  const history: OrderWithPipeline["pipelineHistory"] = [];
  let cursor = new Date(startDate);

  // Default estimated days per stage for future projections
  const DEFAULT_DAYS = [2, 3, 2, 3, 14, 5, 2, 21, 5, 3, 5, 1];

  for (let i = 0; i < ORDER_PIPELINE_STAGES.length; i++) {
    const stageInfo = ORDER_PIPELINE_STAGES[i];
    const stageNum = stageInfo.stage;

    if (stageNum < currentStage) {
      // Completed stages — actual dates
      const days = daysPerStage[i] ?? 3;
      const completedDate = new Date(cursor);
      completedDate.setDate(completedDate.getDate() + days);
      history.push({
        stage: stageNum,
        name: stageInfo.name,
        responsible: stageInfo.responsible,
        completedAt: completedDate.toISOString().split("T")[0],
        daysInStage: days,
      });
      cursor = completedDate;
    } else if (stageNum === currentStage) {
      // Current stage — in progress, show estimated date
      const daysInCurrent = daysPerStage[i] ?? DEFAULT_DAYS[i] ?? 3;
      const estDate = new Date(cursor);
      estDate.setDate(estDate.getDate() + daysInCurrent);
      history.push({
        stage: stageNum,
        name: stageInfo.name,
        responsible: stageInfo.responsible,
        completedAt: null,
        daysInStage: daysInCurrent,
        estimatedDate: estDate.toISOString().split("T")[0],
      });
      cursor = estDate;
    } else {
      // Future stages — estimated dates
      const estDays = DEFAULT_DAYS[i] ?? 3;
      const estDate = new Date(cursor);
      estDate.setDate(estDate.getDate() + estDays);
      history.push({
        stage: stageNum,
        name: stageInfo.name,
        responsible: stageInfo.responsible,
        completedAt: null,
        daysInStage: null,
        estimatedDate: estDate.toISOString().split("T")[0],
      });
      cursor = estDate;
    }
  }

  return history;
}

